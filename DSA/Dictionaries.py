# Creating a dictionary
my_dict = {"name": "Alice", "age": 30, "city": "Nairobi"}

# Accessing values
print(my_dict["name"])  # Output: Alice

# Adding key-value pairs
my_dict["country"] = "Kenya"
print(my_dict)  # Output: {"name": "Alice", "age": 30, "city": "Nairobi", "country": "Kenya"}

# Updating values
my_dict["age"] = 33
print(my_dict)  # Output: {"name": "Alice", "age": 33, "city": "Nairobi", "country": "Kenya"}